#!/usr/bin/env python3

import argparse
import re
import sys

# Reference database mapping by target region
REFERENCE_DB = {
    "16S": "SILVA SSU r138_2",
    "18S": "PR2 v4.13",
    "ITS": "UNITE v2024",
}

def parse_versions(versions_file):
    """Parse versions from versions.txt file."""
    versions = {}
    try:
        with open(versions_file) as f:
            for line in f:
                line_lower = line.lower()
                if 'fastqc' in line_lower:
                    versions['fastqc'] = re.sub(r'^v', '', line.split()[1])
                elif 'multiqc' in line_lower:
                    versions['multiqc'] = re.sub(r'^v', '', line.split()[2])
                elif 'dada2' in line_lower:
                    versions['dada2'] = re.sub(r'^v', '', line.split()[1])
                elif 'decipher' in line_lower:
                    versions['decipher'] = re.sub(r'^v', '', line.split()[1])
                elif 'cutadapt' in line_lower:
                    versions['cutadapt'] = re.sub(r'^v', '', line.split()[1])
                elif 'ancombc' in line_lower:
                    versions['ancombc'] = re.sub(r'^v', '', line.split()[1])
                elif 'deseq' in line_lower:
                    versions['deseq'] = re.sub(r'^v', '', line.split()[1])
                elif 'phyloseq' in line_lower:
                    versions['phyloseq'] = re.sub(r'^v', '', line.split()[1])
                elif re.match(r'^r ', line, re.IGNORECASE):
                    versions['r'] = re.sub(r'^v', '', line.split()[2])
                elif 'vegan' in line_lower:
                    versions['vegan'] = re.sub(r'^v', '', line.split()[1])
                elif 'ggplot2' in line_lower:
                    versions['ggplot2'] = re.sub(r'^v', '', line.split()[1])
    except FileNotFoundError:
        sys.exit(f"Error: versions file '{versions_file}' not found")
    return versions


def parse_trunc_len(trunc_len_str):
    """Parse truncLen string like '230,190' into left and right values."""
    try:
        left, right = trunc_len_str.split(',')
        return left.strip(), right.strip()
    except ValueError:
        sys.exit(f"Error: truncLen must be in format 'left,right' (e.g. '230,190'), got: '{trunc_len_str}'")


def parse_max_ee(max_ee_str):
    """Parse maxEE string like '2,2' into left and right values."""
    try:
        left, right = max_ee_str.split(',')
        return left.strip(), right.strip()
    except ValueError:
        sys.exit(f"Error: maxEE must be in format 'left,right' (e.g. '2,2'), got: '{max_ee_str}'")


def get_reference_db(target_region):
    """Get reference database name for a given target region."""
    db = REFERENCE_DB.get(target_region.upper())
    if db is None:
        sys.exit(f"Error: Unknown target region '{target_region}'. Known regions: {list(REFERENCE_DB.keys())}")
    return db


def generate_protocol(args, versions, rarefaction_depth):
    v = versions  # shorthand

    # --- Paragraph 1: Overview ---
    overview = (
        f"Data were processed as described in {args.protocol_id} "
        f"(https://github.com/nasa/GeneLab_Data_Processing/blob/master/Amplicon/Illumina/Pipeline_GL-DPPD-7104_Versions/{args.protocol_id}.md), "
        f"using workflow NF_AmpIllumina v{args.workflow_version} "
        f"(https://github.com/nasa/GeneLab_AmpliconSeq_Workflow/blob/v{args.workflow_version}/README.md)."
    )

    # --- Paragraph 2: QC (cutadapt sentence conditional) ---
    qc_parts = []
    if args.trim_primers:
        qc_parts.append(f"Primers were removed from raw reads using cutadapt v{v['cutadapt']}.")
    qc_parts.append(
        f"Quality assessment of filtered reads was performed "
        f"with FastQC v{v['fastqc']} and reports were summarized with MultiQC v{v['multiqc']}."
    )
    qc = "Quality control: " + " ".join(qc_parts)

    # --- Paragraph 3: ASV + taxonomy ---
    left_trunc, right_trunc = parse_trunc_len(args.trunc_len)
    left_maxee, right_maxee = parse_max_ee(args.max_ee)
    ref_db = get_reference_db(args.target_region)

    asv = (
        f"ASV table generation and taxonomy assignment: DADA2 v{v['dada2']} was utilized for quality trimming, "
        f"filtering, and inference of amplicon sequence variants (ASVs) with the following parameters: "
        f"truncLen: c({left_trunc}, {right_trunc}), maxEE: c({left_maxee}, {right_maxee}), and truncQ: 2. "
        f"Taxonomy assignment of the ASVs was performed with DECIPHER v{v['decipher']} against the {ref_db} "
        f"reference database. Sample-wise and group-wise relative abundance was calculated and plotted for each taxon level."
    )

    # --- Paragraph 4: Diversity ---
    diversity = (
        f"Diversity analysis: For alpha (within samples) and beta (between samples) analyses, the ASV count table "
        f"generated by DADA2 were normalized using two different methods: variance stabilizing transformation (VST) "
        f"with DESeq2 v{v['deseq']} and rarefaction to even depth with phyloseq v{v['phyloseq']}. "
        f"For normalization using rarefaction, samples were rarefied to even depth at {rarefaction_depth} sequences per sample. "
        f"To estimate alpha-diversity, the total number of observed features, Chao1, Simpson, and Shannon's diversity index (H\u2032) "
        f"and rarefaction curves were calculated using vegan v{v['vegan']}. "
        f"For beta diversity, Bray-Curtis and Euclidean distance matrices were generated using vegan v{v['vegan']} "
        f"on the rarefied and VST normalized ASV tables, respectively."
    )

    # --- Paragraph 5: Statistics ---
    stats = (
        f"Statistical analysis: Statistical analyses and graphical displays were conducted and generated using "
        f"R v{v['r']} and ggplot2 v{v['ggplot2']}. "
        f"Alpha diversity: The non-parametric Wilcoxon rank-sum or Dunn test was used to detect significant differences "
        f"between two or multiple groups, respectively. "
        f"Beta diversity: Principal co-ordinate analysis (PCoA) was performed on both Bray Curtis and Euclidean distances between samples. "
        f"The principal co-ordinates were visualized as scatter plots and distances were visualized as dendrograms using ggplot2. "
        f"The non-parametric permutational multivariate analysis of variance (PERMANOVA) was used to determine the overall changes "
        f"in microbial communities between samples and group clustering with 999 permutations based on both distance matrices "
        f"using the adonis2() function in vegan package v{v['vegan']}."
    )

    # --- Paragraph 6: Differential abundance ---
    diff_abund = (
        f"Differential abundance testing: Differential abundance testing was performed using three different methods: "
        f"ANCOM-BC v{v['ancombc']}, ANCOM-BC2 v{v['ancombc']} and DESeq2 v{v['deseq']}. "
        f"ANCOM-BC was run once for each pair-wise group comparison using a Z-test to generate p-values derived from "
        f"the W statistic in a log-linear regression model with bias correction. "
        f"ANCOM-BC2 utilized a pairwise directional test to perform pair-wise group comparisons. "
        f"DESeq2 was used to perform pair-wise group comparisons using the Wald test with a negative binomial generalized linear model. "
        f"FDR was used to adjust for multiple comparisons testing and to generate an adjusted p-value in all 3 methods. "
        f"To visualize group-wise differences, volcano plots were generated for each pair-wise comparison for each differential abundance method. "
        f"Only ASVs with an adjusted p-value less than or equal to 0.1 were considered significant."
    )

    return "\n\n".join([overview, qc, asv, diversity, stats, diff_abund])


def main():
    parser = argparse.ArgumentParser(description="Generate amplicon processing protocol text.")
    parser.add_argument('versions_file', help='Path to versions.txt')
    parser.add_argument('protocol_id', help='Protocol ID (e.g. GL-DPPD-7104-C)')
    parser.add_argument('--workflow_version', default='1.0.7', help='Workflow version (default: 1.0.7)')
    parser.add_argument('--rarefaction_depth_file', default=None, help='File containing rarefaction depth value')
    parser.add_argument('--target_region', required=True, choices=list(REFERENCE_DB.keys()), help='Target region (e.g. 16S, 18S, ITS)')
    parser.add_argument('--trunc_len', required=True, help='truncLen values as "left,right" (e.g. "230,190")')
    parser.add_argument('--max_ee', required=True, help='maxEE values as "left,right" (e.g. "2,2")')
    parser.add_argument('--trim_primers', action='store_true', help='Include cutadapt primer trimming sentence')

    args = parser.parse_args()

    # Rarefaction depth
    rarefaction_depth = "N/A"
    if args.rarefaction_depth_file:
        try:
            with open(args.rarefaction_depth_file) as f:
                content = f.read().strip()
                if content:
                    rarefaction_depth = content
        except FileNotFoundError:
            pass  # stays N/A

    versions = parse_versions(args.versions_file)
    protocol = generate_protocol(args, versions, rarefaction_depth)
    with open("protocol.txt", "w") as f:
    	f.write(protocol + "\n")


if __name__ == "__main__":
    main()